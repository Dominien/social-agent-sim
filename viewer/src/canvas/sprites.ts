import type { AgentName } from '../api/types'
import { AGENT_STYLE } from './colors'

// Sprite pixel grid: 10 wide x 14 tall
// Each cell is a palette key that maps to the agent's AGENT_STYLE colors
// '_' = transparent, 'H' = hair, 'S' = skin, 'C' = clothes, 'P' = pants, 'X' = shoes, 'D' = detail, 'E' = eye
type PxKey = '_' | 'H' | 'S' | 'C' | 'P' | 'X' | 'D' | 'E'
type SpriteFrame = PxKey[][]

const PALETTE_MAP: Record<PxKey, keyof typeof AGENT_STYLE.marco | null> = {
  '_': null, 'H': 'hair', 'S': 'skin', 'C': 'clothes', 'P': 'pants', 'X': 'shoes', 'D': 'hair', 'E': null,
}

function resolveColor(key: PxKey, agent: AgentName): string | null {
  if (key === '_') return null
  if (key === 'E') return '#1a1a1a'
  const prop = PALETTE_MAP[key]
  if (!prop) return null
  return AGENT_STYLE[agent][prop] ?? null
}

